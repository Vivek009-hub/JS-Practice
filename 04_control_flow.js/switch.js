const month = 3
switch (month) {
    case 1:
        console.log('January');

        break;
    case 2:
        console.log('Feb');
        break
    case 3:
        console.log('Mar');
        // break              // now print all remaining cases except Default case
    case 3:
        console.log('Apr');
        break
    default:
        console.log('default case mathched');

        break;
}


